public class ResourceResponse {

    private String name;

    private String email;

    private String mobNumber;

    private String dob;

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getMobNumber() {
        return mobNumber;
    }

    public String getDob() {
        return dob;
    }
}
